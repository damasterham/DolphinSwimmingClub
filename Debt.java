package DolhpinSwimClubDCD;

public class Debt {

	private String name;
	private double debt;

	public String getName() {
		return this.name;
	}

	/**
	 * 
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}

	public double getDebt() {
		return this.debt;
	}

	/**
	 * 
	 * @param debt
	 */
	public void setDebt(double debt) {
		this.debt = debt;
	}

}